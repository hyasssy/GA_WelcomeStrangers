function artistinfo(num) {
    var data = _captiondata[num];
    var root = document.createElement("div");
    root.className = "artistinfo-root";
    root.id = data.id;

    for (var i = 0; i < data.text.length; i++) {
        var div = document.createElement("div");
        div.id = data.text[i].id;
        div.className = data.text[i].class;
        root.appendChild(div);
    }

    var statementdata = data.statement;
    if (statementdata != "none") {
        var a = document.createElement("a");
        a.href = statementdata.href;
        a.id = statementdata.id;
        a.textContent = "STATEMENT";
        a.target = "_blank";
        root.appendChild(a);
    }
    return root;
}
function setartistsinfo(target) {
    for (var i = 0; i < _captiondata.length; i++) {
        target.appendChild(artistinfo(i));
    }
}

//改行やスペースをコードに落とし込んだstringに変換する。
function ht_str(str) {
    if (str == null) return '';
    str = str.toString();
    str = str.replace(/&/g, '&amp;');
    str = str.replace(/</g, '&lt;');
    str = str.replace(/>/g, '&gt;');
    // str = str.replace( / /g,'&nbsp;' );
    // str = str.replace( /\t/g,'&nbsp;&nbsp;&nbsp;&nbsp;' ); // Tabをスペース4つに..
    str = str.replace(/\r?\n/g, "<br />\n");
    return str;
}